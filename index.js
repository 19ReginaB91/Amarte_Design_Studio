    // –ñ–µ—Å—Ç–∫–æ —É—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞–µ–º DE, –ø–æ–∫–∞ –¥—Ä—É–≥–∏–µ —è–∑—ã–∫–∏ –æ—Ç–∫–ª—é—á–µ–Ω—ã
    const effectiveLang = 'de';

    applyTranslations(effectiveLang);

    // –õ–æ–≥–∏–∫–∞ –∞–∫—Ç–∏–≤–∞—Ü–∏–∏ –∫–Ω–æ–ø–∫–∏ DE (—É–¥–∞–ª—è–µ–º –∫–ª–∞—Å—Å—ã —Å–æ –≤—Å–µ—Ö, –¥–æ–±–∞–≤–ª—è–µ–º —Ç–æ–ª—å–∫–æ DE)
    document.querySelectorAll('.lang-button').forEach(btn => {
        btn.classList.remove('active');
    });

    const activeBtn = document.querySelector(`#lang-de`);
    if (activeBtn) {
        activeBtn.classList.add('active');
    }

    // –°–æ—Ö—Ä–∞–Ω–µ–Ω–∏–µ —è–∑—ã–∫–∞ (–≤—Ä–µ–º–µ–Ω–Ω–æ DE)
